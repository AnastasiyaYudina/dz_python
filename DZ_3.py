# Напишите программу, которая вычисляет стоимость 
# введенного пользователем слова. Будем считать, 
# что на вход подается только одно слово, 
# которое содержит либо только английские, либо только русские буквы.

dict_eng={1:'AEIOULNSTR', 2:'DG', 3:'BCMP', 4:'FHVWY',5:'K',8:'JX',10:'QZ' }
dict_rus={1:'АВЕИНОРСТ',2:'ДКЛМПУ',3:'БГЁЬЯ',4:'ЙЫ',5:'ЖЗХЦЧ',8:'ШЭЮ',10:'ФЩЪ'}
rus='АВЕИНОРСТДКЛМПУБГЁЬЯЙЫЖЗХЦЧШЭЮФЩЪ'
eng='AEIOULNSTRDGBCMPFHVWYKJXQZ'
word=input('Введите слово на русском или английском языке: ')


if word[0].upper() in eng:
  s=0
  for letter in word:
       for key, value in dict_eng.items():
        if letter.upper() in value:
           s+=key
  print(f'Стоимость введенного слова  на английском: {s}')         
else:
   if word[0].upper() in rus:
    s=0
    for letter in word:
      for key, value in dict_rus.items():
         if letter.upper() in value:
            s+=key
    print(f'Стоимость введенного слова русском: {s}')     

    
